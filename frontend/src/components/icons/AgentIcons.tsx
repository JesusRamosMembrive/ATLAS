// ============================================================================
// Tool Icon Mapping Function
// ============================================================================

/**
 * Get the appropriate icon component for a tool name
 */
export function getToolIconComponent(toolName: string): React.FC<IconProps> {
  // Exact matches
  const exactMatches: Record<string, React.FC<IconProps>> = {
    Read: FileTextIcon,
    Write: PencilIcon,
    Edit: EditIcon,
    Bash: TerminalIcon,
    Glob: SearchIcon,
    Grep: SearchIcon,
    Task: ClipboardListIcon,
    TodoWrite: CheckSquareIcon,
    WebFetch: GlobeIcon,
    WebSearch: GlobeIcon,
    NotebookEdit: NotebookIcon,
  };

  if (exactMatches[toolName]) {
    return exactMatches[toolName];
  }

  // Prefix matches for MCP tools
  if (toolName.startsWith("mcp__playwright")) return TheaterIcon;
  if (toolName.startsWith("mcp__context7")) return BookIcon;
  if (toolName.startsWith("mcp__sequential")) return BrainCogIcon;
  if (toolName.startsWith("mcp__magic")) return SparklesIcon;
  if (toolName.startsWith("mcp__chrome")) return WrenchIcon;

  // Default
  return WrenchIcon;
}
